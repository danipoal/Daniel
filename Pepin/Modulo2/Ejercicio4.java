

//Programa que tenga imprima los numeros de dos cifras que sean multiplos de 7
public class DosCifras {
	
	public static void main (String[] args) {
		System.out.println("Los numeros de dos cifras multiplos de 7 son:");
		for(int i=14; i <100; i+=7){
				System.out.println(i);
		}
	}
}

